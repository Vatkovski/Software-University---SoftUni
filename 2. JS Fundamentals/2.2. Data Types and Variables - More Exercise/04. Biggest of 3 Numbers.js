function findBiggestNumber(a, b, c) {
    return Math.max(a, b, c);
}

console.log(findBiggestNumber(-2, 7, 3)); // Output: 7
console.log(findBiggestNumber(130, 5, 99)); // Output: 130
console.log(findBiggestNumber(43, 43.2, 43.1)); // Output: 43.2
console.log(findBiggestNumber(2, 2, 2)); // Output: 2
